// Generates lib/posts/index.ts from every lib/posts/<slug>.ts data file.
// Each data file must `export const post: BlogPost = { ... }`.
// Run: node scripts/gen-posts-index.mjs
import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const postsDir = join(__dirname, "..", "lib", "posts");
const EXCLUDE = new Set(["index.ts", "helpers.ts"]);

const files = readdirSync(postsDir)
  .filter((f) => f.endsWith(".ts") && !EXCLUDE.has(f))
  .map((f) => f.replace(/\.ts$/, ""))
  .sort();

const varName = (slug, i) => "p" + i;

const imports = files
  .map((slug, i) => `import { post as ${varName(slug, i)} } from './${slug}';`)
  .join("\n");

const entries = files
  .map((slug, i) => `  [${varName(slug, i)}.slug]: ${varName(slug, i)},`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-posts-index.mjs — do not edit by hand.
// Registry of all ported Wix blog posts, served at /post/<slug>.
import type { BlogPost } from "@/lib/blog-types";
${imports}

export const POST_REGISTRY: Record<string, BlogPost> = {
${entries}
};

export * from "./helpers";
`;

writeFileSync(join(postsDir, "index.ts"), out);
console.log(`Wrote lib/posts/index.ts with ${files.length} posts.`);
